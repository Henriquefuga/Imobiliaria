
public class Apartamento extends Casa{
	private int academia;
	private int aptPorAndar;
	private int sacada;
	private int andar;
	public int getAcademia() {
		return academia;
	}
	public void setAcademia(int academia) {
		this.academia = academia;
	}
	public int getAptPorAndar() {
		return aptPorAndar;
	}
	public void setAptPorAndar(int aptPorAndar) {
		this.aptPorAndar = aptPorAndar;
	}
	public int getSacada() {
		return sacada;
	}
	public void setSacada(int sacada) {
		this.sacada = sacada;
	}
	public int getAndar() {
		return andar;
	}
	public void setAndar(int andar) {
		this.andar = andar;
	}
}
